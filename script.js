    document.addEventListener("DOMContentLoaded", function () {
        const langSwitcher = document.getElementById("language-switcher");

        function detectBrowserLanguage() {
            const userLang = navigator.language || navigator.userLanguage;
            const supportedLanguages = { "uk": "ua", "ru": "ua", "en": "en" };
            return supportedLanguages[userLang.split('-')[0]] || "ua";
        }

        // üîπ –ó–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è –º–æ–≤–∏ –∑ LocalStorage –∞–±–æ –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω–µ –≤–∏–∑–Ω–∞—á–µ–Ω–Ω—è
        const savedLang = localStorage.getItem("selectedLanguage") || detectBrowserLanguage();
        langSwitcher.value = savedLang;
        updateLanguage(savedLang);

        // üîπ –î–æ–¥–∞—î–º–æ –æ–±—Ä–æ–±–Ω–∏–∫ –ø–æ–¥—ñ–π –Ω–∞ –≤–∏–ø–∞–¥–∞—é—á–∏–π —Å–ø–∏—Å–æ–∫
        langSwitcher.addEventListener("change", function () {
            updateLanguage(this.value);
        });
    });

    function updateLanguage(lang) {
        const elements = document.querySelectorAll("[data-lang]");
        const nicheSelect = document.getElementById("niche");
        const counterTextElement = document.querySelector("[data-lang='counterText']");
        const counterElement = document.getElementById("counter");
        const termsSections = document.querySelectorAll("[data-term-section]");
        const emailInput = document.getElementById("email");

        // üîπ –û–Ω–æ–≤–ª—é—î–º–æ –≤—Å—ñ –µ–ª–µ–º–µ–Ω—Ç–∏ –∑ data-lang
        elements.forEach(el => {
            const key = el.getAttribute("data-lang");
            if (translations[lang] && translations[lang][key]) {
               el.innerHTML = decodeEntities(translations[lang][key]);
            }
        });

        const currentPage = window.location.pathname;
        // –õ–æ–≥—ñ–∫–∞ –≤–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Å—Ç–æ—Ä—ñ–Ω–∫–∏
        let pageKey;
        if (currentPage.includes("terms")) {
            pageKey = "terms";
        } else if (currentPage.includes("privacy")) {
            pageKey = "privacy";
        } else {
            pageKey = "landing";
        }

        // –û–Ω–æ–≤–ª–µ–Ω–Ω—è title
        if (translations[lang][`${pageKey}PageTitle`]) {
            document.title = translations[lang][`${pageKey}PageTitle`];
        }

        // –û–Ω–æ–≤–ª–µ–Ω–Ω—è meta description
        const metaDesc = document.querySelector("meta[name='description']");
        if (metaDesc && translations[lang][`${pageKey}PageDescription`]) {
            metaDesc.setAttribute("content", translations[lang][`${pageKey}PageDescription`]);
        }

        // –û–Ω–æ–≤–ª–µ–Ω–Ω—è Open Graph title
        const ogTitle = document.querySelector("meta[property='og:title']");
        if (ogTitle && translations[lang][`${pageKey}PageTitle`]) {
            ogTitle.setAttribute("content", translations[lang][`${pageKey}PageTitle`]);
        }

        // –û–Ω–æ–≤–ª–µ–Ω–Ω—è Open Graph description
        const ogDesc = document.querySelector("meta[property='og:description']");
        if (ogDesc && translations[lang][`${pageKey}PageDescription`]) {
            ogDesc.setAttribute("content", translations[lang][`${pageKey}PageDescription`]);
        }

        // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Ç–µ–∫—Å—Ç—É –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        if (counterTextElement && counterElement) {
            counterTextElement.innerHTML = `üî• <span id="counter">${counterElement.innerText}</span> ${translations[lang]['counterText']}`;
        }

        if (emailInput) {
            emailInput.placeholder = translations[lang]["enterEmail"];
        }

        // üîπ –û—á–∏—â—É—î–º–æ —Å–ø–∏—Å–æ–∫ –Ω—ñ—à –ø–µ—Ä–µ–¥ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è–º
        if (nicheSelect) {
            nicheSelect.innerHTML = "";

            // üîπ –î–æ–¥–∞—î–º–æ "–í–∏–±–µ—Ä—ñ—Ç—å..." —è–∫ –ø–µ—Ä—à–∏–π –≤–∞—Ä—ñ–∞–Ω—Ç
            const defaultOption = document.createElement("option");
            defaultOption.textContent = lang === "ua" ? "–í–∏–±–µ—Ä—ñ—Ç—å..." : "Choose...";
            defaultOption.disabled = true;
            defaultOption.selected = true;
            nicheSelect.appendChild(defaultOption);

            // üîπ –î–æ–¥–∞—î–º–æ –≥—Ä—É–ø–∏ –Ω—ñ—à
            const popularGroup = document.createElement("optgroup");
            popularGroup.label = translations[lang]["niches"]["popular"];
            nicheSelect.appendChild(popularGroup);

            const specializedGroup = document.createElement("optgroup");
            specializedGroup.label = translations[lang]["niches"]["specialized"];
            nicheSelect.appendChild(specializedGroup);

            // üîπ –î–æ–¥–∞—î–º–æ –æ–ø—Ü—ñ—ó —É –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω—ñ –≥—Ä—É–ø–∏
            translations[lang]["niches"]["options"].forEach(option => {
                const optElement = document.createElement("option");
                optElement.value = option.value;
                optElement.textContent = option.text;

                if (option.group === "popular") {
                    popularGroup.appendChild(optElement);
                } else {
                    specializedGroup.appendChild(optElement);
                }
            });
        }

        Object.keys(translations[lang]["termsSections"]).forEach(sectionKey => {
            const section = translations[lang]["termsSections"][sectionKey];

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∑–∞–≥–æ–ª–æ–≤–∫–∞
            const titleElement = document.querySelector(`[data-lang="termsSections.${sectionKey}.title"]`);
            if (titleElement) {
                titleElement.innerHTML = decodeEntities(section.title);
            }

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∫–æ–Ω—Ç–µ–Ω—Ç—É
            const contentElement = document.querySelector(`[data-lang="termsSections.${sectionKey}.content"]`);
            if (contentElement) {
                contentElement.innerHTML = decodeEntities(section.content);
            }

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Å–ø–∏—Å–∫—É (—è–∫—â–æ –≤—ñ–Ω —î –≤ —Å–µ–∫—Ü—ñ—ó)
            const listElement = document.getElementById(`termsSections.${sectionKey}.list`);
            if (listElement && section.list) {
                listElement.innerHTML = ""; // –û—á–∏—â–µ–Ω–Ω—è –ø–µ—Ä–µ–¥ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è–º
                section.list.forEach(item => {
                    const li = document.createElement("li");
                    li.textContent = item;
                    listElement.appendChild(li);
                });
            }
        });

        Object.keys(translations[lang]["privacySections"]).forEach(sectionKey => {
            const section = translations[lang]["privacySections"][sectionKey];

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∑–∞–≥–æ–ª–æ–≤–∫–∞
            const titleElement = document.querySelector(`[data-lang="privacySections.${sectionKey}.title"]`);
            if (titleElement) {
                titleElement.innerHTML = decodeEntities(section.title);
            }

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∫–æ–Ω—Ç–µ–Ω—Ç—É
            const contentElement = document.querySelector(`[data-lang="privacySections.${sectionKey}.content"]`);
            if (contentElement) {
                contentElement.innerHTML = decodeEntities(section.content);
            }

            // üîπ –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Å–ø–∏—Å–∫—É (—è–∫—â–æ –≤—ñ–Ω —î –≤ —Å–µ–∫—Ü—ñ—ó)
            const listElement = document.getElementById(`privacySections.${sectionKey}.list`);
            if (listElement && section.list) {
                listElement.innerHTML = ""; // –û—á–∏—â–µ–Ω–Ω—è –ø–µ—Ä–µ–¥ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è–º
                section.list.forEach(item => {
                    const li = document.createElement("li");
                    li.textContent = item;
                    listElement.appendChild(li);
                });
            }
        });

        // üîπ –ó–±–µ—Ä—ñ–≥–∞—î–º–æ –≤–∏–±—Ä–∞–Ω—É –º–æ–≤—É —É LocalStorage
        localStorage.setItem("selectedLanguage", lang);
    }

    function decodeEntities(encodedString) {
        const textArea = document.createElement("textarea");
        textArea.innerHTML = encodedString;
        return textArea.value;
    }

